#ifndef ARRAY_H
#define ARRAY_H

#define N 200  // Максимальный размер массива

/**
 * Функция для вычисления значения f(x) = 5x³ + 2x² + 15x - 6
 * @param x аргумент функции
 * @return значение функции
 */
double func(double x);

/**
 * Заполнение массива значениями функции
 * @param a указатель на массив
 * @param n размер массива
 * @return указатель на заполненный массив
 */
double* full_elements(double* a, int n);

/**
 * Печать элементов массива
 * @param a указатель на массив
 * @param n размер массива
 * @return 0 в случае успешного выполнения
 */
int put_elements(double* a, int n);

/**
 * Вычисление суммы элементов массива на заданном интервале
 * @param a указатель на массив
 * @param b начальный индекс
 * @param e конечный индекс
 * @return сумма элементов
 */
double sum_elements(double* a, int b, int e);

/**
 * Поиск элемента в массиве
 * @param a указатель на массив
 * @param n размер массива
 * @param v искомое значение
 * @return индекс найденного элемента или -1 если не найден
 */
int find_element(double* a, int n, double v);

/**
 * Преобразование массива: добавление значения последнего элемента ко всем элементам
 * @param a указатель на массив
 * @param n размер массива
 * @return указатель на преобразованный массив
 */
double* calc_elements(double* a, int n);

/**
 * Поиск индекса минимального положительного элемента больше заданного значения A
 * @param a указатель на массив
 * @param n размер массива
 * @param A заданное значение
 * @return индекс найденного элемента или -1 если не найден
 */
int find_min_positive_greater_than_A(double* a, int n, double A);

/**
 * Поиск индекса минимального элемента в массиве
 * @param a указатель на массив
 * @param n размер массива
 * @return индекс минимального элемента или -1 если массив пуст
 */
int find_min_element_index(double* a, int n);

/**
 * Вставка k элементов -999 после минимального элемента
 * @param a указатель на массив
 * @param n указатель на размер массива (будет изменен)
 * @param k количество элементов для вставки
 */
void insert_after_min(double* a, int* n, int k);

#endif
